import React, { useState, useRef } from "react";
import { useNavigate } from "react-router-dom";

export default function SupportSection() {
  const navigate = useNavigate();
  const [sosActive, setSosActive] = useState(false);
  const audioRef = useRef(null);

  const handleCall = (type, number) => {
    navigate(`/emergency?type=${type}&number=${number}`);
  };

  const playSirenSound = () => {
    console.log('Starting EMERGENCY alarm - Enthiran BGM...');

    if (!audioRef.current) {
      // Assuming the user will place the file 'enthiran_bgm.mp3' in the public folder
      audioRef.current = new Audio('/enthiran_bgm.mp3');
      audioRef.current.loop = true;
      audioRef.current.volume = 1.0;
    }

    audioRef.current.play().catch(error => {
      console.error('Failed to play alarm:', error);
      alert('Background music file not found! Please add "enthiran_bgm.mp3" to your public folder.');
    });
  };

  const stopSirenSound = () => {
    if (audioRef.current) {
      audioRef.current.pause();
      audioRef.current.currentTime = 0;
    }
  };

  const handleSOS = () => {
    console.log('SOS button clicked');

    if (sosActive) {
      // Stop SOS
      setSosActive(false);
      stopSirenSound();
    } else {
      // Activate SOS
      setSosActive(true);

      // Play ambulance siren (Enthiran BGM)
      playSirenSound();
    }
  };

  return (
    <div className="card emergency-support-card">
      <h3>ðŸ†˜ Emergency Support</h3>
      <p style={{ marginBottom: '20px', color: 'var(--text-light)' }}>
        Quickly connect with emergency services in case of any incident.
      </p>

      {/* SOS Alarm Button */}
      <div className="sos-container">
        <button
          className={`sos-button ${sosActive ? 'active' : ''}`}
          onClick={handleSOS}
        >
          <span className="sos-icon">ðŸš¨</span>
          <span className="sos-text">{sosActive ? 'STOP SOS ALARM' : 'SOS ALARM'}</span>
        </button>
        {sosActive && (
          <p className="sos-status">ðŸ”´ SOS Active - Emergency alert sent!</p>
        )}
      </div>

      <div className="emergency-buttons">
        <div className="emergency-item" onClick={() => handleCall('Police', '100')}>
          <div className="emergency-icon-circle police">ðŸ‘®</div>
          <div className="emergency-info">
            <h4>Police</h4>
            <span className="emergency-number">Call 100</span>
          </div>
        </div>

        <div className="emergency-item" onClick={() => handleCall('Hospital', '108')}>
          <div className="emergency-icon-circle hospital">ðŸš‘</div>
          <div className="emergency-info">
            <h4>Hospital / Ambulance</h4>
            <span className="emergency-number">Call 108</span>
          </div>
        </div>
      </div>

      <style>{`
        .sos-container {
          margin-bottom: 25px;
          text-align: center;
        }
        .sos-button {
          width: 100%;
          max-width: 300px;
          padding: 20px;
          background: linear-gradient(135deg, #e53e3e 0%, #c53030 100%);
          border: none;
          border-radius: 15px;
          color: white;
          font-size: 1.2rem;
          font-weight: 700;
          cursor: pointer;
          transition: all 0.3s ease;
          box-shadow: 0 4px 15px rgba(229, 62, 62, 0.4);
          display: flex;
          align-items: center;
          justify-content: center;
          gap: 10px;
          margin: 0 auto;
        }
        .sos-button:hover {
          transform: translateY(-3px);
          box-shadow: 0 6px 20px rgba(229, 62, 62, 0.6);
        }
        .sos-button.active {
          background: linear-gradient(135deg, #38a169 0%, #2f855a 100%);
          animation: sosFlash 1s infinite;
        }
        .sos-icon {
          font-size: 1.5rem;
          animation: sosRotate 2s infinite;
        }
        .sos-status {
          margin-top: 10px;
          color: #e53e3e;
          font-weight: 600;
          animation: pulse 1s infinite;
        }
        @keyframes sosFlash {
          0%, 100% { opacity: 1; }
          50% { opacity: 0.7; }
        }
        @keyframes sosRotate {
          0% { transform: rotate(0deg); }
          25% { transform: rotate(-15deg); }
          75% { transform: rotate(15deg); }
          100% { transform: rotate(0deg); }
        }
        @keyframes pulse {
          0%, 100% { transform: scale(1); }
          50% { transform: scale(1.05); }
        }
        .emergency-buttons {
          display: flex;
          flex-direction: column;
          gap: 15px;
          max-width: 500px;
          margin: 0 auto;
        }
        .emergency-item {
          display: flex;
          align-items: center;
          gap: 20px;
          padding: 15px 25px;
          background: var(--background-light);
          border-radius: 15px;
          cursor: pointer;
          transition: all 0.3s ease;
          border: 1px solid var(--border-color);
          text-align: left;
        }
        .emergency-item:hover {
          transform: translateY(-3px);
          box-shadow: var(--shadow-md);
          background: var(--white);
          border-color: var(--primary-color);
        }
        .emergency-icon-circle {
          width: 60px;
          height: 60px;
          border-radius: 50%;
          display: flex;
          align-items: center;
          justify-content: center;
          font-size: 30px;
          background: white;
          box-shadow: var(--shadow-sm);
        }
        .emergency-icon-circle.police {
          background: #e3f2fd;
        }
        .emergency-icon-circle.hospital {
          background: #fff5f5;
        }
        .emergency-info h4 {
          margin: 0;
          font-size: 1.1rem;
          color: var(--text-dark);
        }
        .emergency-info .emergency-number {
          font-weight: 700;
          color: var(--primary-color);
          font-size: 0.9rem;
        }
      `}</style>
    </div>
  );
}
