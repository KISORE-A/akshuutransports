import React, { useState, useEffect } from "react";
import { updateLocation } from "../services/api";

export default function DriverTracking() {
    const [tracking, setTracking] = useState(false);
    const [status, setStatus] = useState("Inactive");
    const [position, setPosition] = useState(null);
    const [watchId, setWatchId] = useState(null);

    useEffect(() => {
        return () => {
            if (watchId) navigator.geolocation.clearWatch(watchId);
        };
    }, [watchId]);

    const startTracking = () => {
        if (!navigator.geolocation) {
            alert("Geolocation is not supported by your browser");
            return;
        }

        setTracking(true);
        setStatus("Initializing GPS...");

        const id = navigator.geolocation.watchPosition(
            async (pos) => {
                const { latitude, longitude } = pos.coords;
                setPosition({ lat: latitude, lng: longitude });
                setStatus("Tracking Active - Sending updates...");

                try {
                    await updateLocation(latitude, longitude);
                    setStatus(`Live: ${latitude.toFixed(4)}, ${longitude.toFixed(4)}`);
                } catch (err) {
                    console.error(err);
                    setStatus("Error sending data");
                }
            },
            (err) => {
                console.error(err);
                setStatus("GPS Error: " + err.message);
                setTracking(false);
            },
            { enableHighAccuracy: true, timeout: 5000, maximumAge: 0 }
        );
        setWatchId(id);
    };

    const stopTracking = () => {
        if (watchId) {
            navigator.geolocation.clearWatch(watchId);
            setWatchId(null);
        }
        setTracking(false);
        setStatus("Stopped");
        setPosition(null);
    };

    return (
        <div className="card">
            <h3>üìç Live Tracker</h3>
            <div style={{ display: "flex", flexDirection: "column", gap: "12px" }}>
                <div style={{
                    padding: "12px",
                    background: tracking ? "var(--status-bg)" : "var(--background-light)",
                    borderRadius: "6px",
                    textAlign: "center",
                    border: "1px solid var(--border-color)",
                    color: "var(--text-dark)"
                }}>
                    <strong>Status:</strong> {status}
                </div>

                {!tracking ? (
                    <button onClick={startTracking} className="success" style={{ width: "100%" }}>
                        Start Sharing Location
                    </button>
                ) : (
                    <button onClick={stopTracking} className="danger" style={{ width: "100%" }}>
                        Stop Sharing
                    </button>
                )}
            </div>
        </div>
    );
}
